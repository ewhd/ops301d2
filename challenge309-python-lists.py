# Declare Variables
list_var = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j']


# Main

# This line prints the 4th element in the list, with index 3
print(list_var[3])

# This line print the sixth through tenth elements on the list
print(list_var[5:10])

# This line changes the value of the seventh element to "onion"
list_var[6] = "onion"
print(list_var[6])


